package com.jatora.tfg_the_climb_within;

public class Deck {
    private String name;
    private String color;
    private String coin;
    private String[] cards;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getColor() {
        return color;
    }

    public String getCoin() {
        return coin;
    }

    public String[] getCards() {
        return cards;
    }
}
